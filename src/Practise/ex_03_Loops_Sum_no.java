package Practise;

public class ex_03_Loops_Sum_no {
    public static void main(String[] args) {
       // Take a number N and use a loop to calculate the sum of numbers from 1 to N.
      int N=20;
      int sum = 0;
      for(int i=1; i<=N ; i++){
        sum = sum+i;

      }
        System.out.println(sum);
        }
    }

