package Practise;

public class ex_07_Loops {
    public static void main(String[] args) {
        // Print Number Pyramid
        for(int i=1 ;i<6  ;i++  ){
            for(int j=1; j<=i  ; j++){
                System.out.print(j);
            }
            System.out.println();

        }
    }
}
