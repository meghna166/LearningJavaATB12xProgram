package ex_03_Operators;

public class Lab_018_Airthmatic_Operators {
    public static void main(String[] args) {
        int a = 25;
        int b = 36;
        float c = 2.5f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        System.out.println(a+c);
        System.out.println(a%c);
        System.out.println(a/c);



    }
}
