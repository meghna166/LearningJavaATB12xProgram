package ex_03_Operators;

public class Lab_019_Relational_operators {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        boolean c = a > b;
        System.out.println(c);

        boolean d = a < b;
        System.out.println(d);

        boolean e = a <= b;
        System.out.println(e);

        boolean f = a >= b;
        System.out.println(f);


    }
}
