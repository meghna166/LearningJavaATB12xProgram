package ex_08_For_loop;

public class Lab_036_For_If {
    public static void main(String[] args) {
        for(int i =1; i<=18;i++){
            if(i<18){
                System.out.println("You can not vote");
            }
            else{
                System.out.println("you can vote");
            }
        }
    }
}
